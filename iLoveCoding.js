let angka = 1;
for (let angka = 1; angka <= 20; angka++) {
  console.log(angka + " - I love coding Looping1");
}

let dua = 20;
for (let i = dua; i >= 1; i--) {
  console.log(i + " - I love coding Loopin2");
}

